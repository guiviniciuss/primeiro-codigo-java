package br.com.senac.primeirocodigo;

public class ImprimindoTexto {

	public static void main(String[] args) {
		// Texto que será impresso no console.
		System.out.println("Eu sou um excelente desenvolvedor, "
				+ "serei um excelente profissional, "
				+ "preciso estudar muito, "
				+ "serei um eterno aprendiz "
				+ "e só sei que nada sei, "
				+ "porque no livro da humildade "
				+ "a primeira assinatura tem que "
				+ "ser minha.\n");
		
		// Cálculo matemático, o resultado será impresso no console.
		System.out.println(77*77);

	}

}